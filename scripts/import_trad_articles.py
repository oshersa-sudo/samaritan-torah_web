def main():
    conn = sqlite3.connect(DB, timeout=60); conn.row_factory = sqlite3.Row
    vidx = {}
    for r in conn.execute("""SELECT v.id vid, b.name bk, c.number ch, v.number vn
                             FROM verses v JOIN chapters c ON c.id=v.chapter_id
                             JOIN books b ON b.id=c.book_id"""):
        if str(r['vn']).isdigit():
            vidx[(r['bk'], r['ch'], int(r['vn']))] = r['vid']

    resolved, missing = [], []
    for title, author, ordn, text, anchors in SECTIONS:
        vids = []
        for (bk, ch, vn) in anchors:
            vid = vidx.get((bk, ch, vn))
            if vid:
                vids.append(vid)
            else:
                missing.append('%s %d:%d' % (bk, ch, vn))
        resolved.append((title, author, ordn, text, vids))
        print('  [%d] %s  → %d/%d verses' % (ordn, title[:48], len(vids), len(anchors)))
    if missing:
        print('\nanchors NOT in DB (%d):' % len(missing), missing)
    print('\nsections:', len(resolved),
          '| total links:', sum(len(v) for *_, v in resolved))

    if not APPLY:
        print('\n[dry-run] re-run with --apply to write'); conn.close(); return

    bak = DB + '.bak_tradart_' + datetime.datetime.now().strftime('%Y%m%d_%H%M%S')
    shutil.copy2(DB, bak); print('backed up ->', os.path.basename(bak))
    cu = conn.cursor()
    cu.execute('DROP TABLE IF EXISTS tradart_verse_links')
    cu.execute('DROP TABLE IF EXISTS tradart_sections')
    cu.execute('CREATE TABLE tradart_sections (id INTEGER PRIMARY KEY, title TEXT, '
               'author TEXT, ord INTEGER, text TEXT)')
    cu.execute('CREATE TABLE tradart_verse_links (id INTEGER PRIMARY KEY, '
               'verse_id INTEGER, section_id INTEGER)')
    for title, author, ordn, text, vids in resolved:
        cu.execute('INSERT INTO tradart_sections (title, author, ord, text) '
                   'VALUES (?,?,?,?)', (title, author, ordn, text))
        sid = cu.lastrowid
        for vid in vids:
            cu.execute('INSERT INTO tradart_verse_links (verse_id, section_id) '
                       'VALUES (?,?)', (vid, sid))
    cu.execute('CREATE INDEX ix_tradart_verse ON tradart_verse_links (verse_id)')
